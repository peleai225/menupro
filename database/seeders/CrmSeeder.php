    private function getCommercialForCity(string $city): User
    {
        // Map cities to commercials based on their zones
        $cityToCommercial = [
            'Cocody' => [0, 1, 5], // Team Abidjan Nord
            'Plateau' => [0, 1, 5],
            'Marcory' => [2, 6], // Team Abidjan Sud
            'Treichville' => [3, 2],
            'Bouaké' => [4, 7], // Team Bouaké
        ];

        $possibleCommercials = $cityToCommercial[$city] ?? [0];
        $index = $possibleCommercials[array_rand($possibleCommercials)];

        return $this->commercials[$index];
    }

