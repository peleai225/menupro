    private function createCommissions(): void
    {
        $this->command->info('Creating commissions...');

        // Get leads that reached SIGNATURE or beyond
        $convertedLeads = collect($this->leads)
            ->filter(fn($lead) => in_array($lead->status, [
                LeadStatus::SIGNATURE,
                LeadStatus::INSTALLATION,
                LeadStatus::ACTIF
            ]));

        foreach ($convertedLeads as $lead) {
            $commercial = User::find($lead->assigned_to);
            $wallet = $commercial->crmWallet;

            // Commission SIGNATURE
            Commission::create([
                'wallet_id' => $wallet->id,
                'user_id' => $commercial->id,
                'type' => CommissionType::SIGNATURE,
                'status' => CommissionStatus::VALIDATED,
                'amount_cents' => 50000, // 50,000 FCFA
                'source_type' => Lead::class,
                'source_id' => $lead->id,
                'description' => "Commission signature - {$lead->restaurant_name}",
                'metadata' => ['lead_id' => $lead->id],
                'validated_at' => now()->subDays(rand(1, 10)),
            ]);

            // Commission INSTALLATION (if lead has installation)
            if (in_array($lead->status, [LeadStatus::INSTALLATION, LeadStatus::ACTIF])) {
                Commission::create([
                    'wallet_id' => $wallet->id,
                    'user_id' => $commercial->id,
                    'type' => CommissionType::INSTALLATION,
                    'status' => $lead->status === LeadStatus::ACTIF ? CommissionStatus::PAID : CommissionStatus::VALIDATED,
                    'amount_cents' => 75000, // 75,000 FCFA
                    'source_type' => Lead::class,
                    'source_id' => $lead->id,
                    'description' => "Commission installation - {$lead->restaurant_name}",
                    'metadata' => ['lead_id' => $lead->id],
                    'validated_at' => now()->subDays(rand(1, 5)),
                ]);
            }
        }

        // Add some bonus commissions for top performers
        $topCommercials = collect($this->commercials)->random(2);
        foreach ($topCommercials as $commercial) {
            $wallet = $commercial->crmWallet;
            Commission::create([
                'wallet_id' => $wallet->id,
                'user_id' => $commercial->id,
                'type' => CommissionType::BONUS_PERFORMANCE,
                'status' => CommissionStatus::PAID,
                'amount_cents' => 100000, // 100,000 FCFA
                'source_type' => null,
                'source_id' => null,
                'description' => 'Bonus performance mensuel',
                'metadata' => ['period' => now()->format('Y-m')],
                'validated_at' => now()->subDays(rand(5, 15)),
            ]);
        }
    }

    private function createWithdrawals(): void
    {
        $this->command->info('Creating withdrawals...');

        // Create 3 pending withdrawals
        $commercials = collect($this->commercials)->random(3);

        foreach ($commercials as $commercial) {
            $wallet = $commercial->crmWallet;
            $amount = rand(50000, min(200000, $wallet->balance_cents));

            Withdrawal::create([
                'wallet_id' => $wallet->id,
                'user_id' => $commercial->id,
                'amount_cents' => $amount,
                'status' => WithdrawalStatus::PENDING,
                'payment_method' => [
                    PaymentMethod::WAVE,
                    PaymentMethod::ORANGE_MONEY,
                    PaymentMethod::MTN,
                ][rand(0, 2)],
            ]);

            // Deduct from wallet (balance is debited when withdrawal is requested)
            $wallet->decrement('balance_cents', $amount);
        }
    }

